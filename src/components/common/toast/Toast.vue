<template>
  <div class="toast" v-show="isShow">
    <div>{{message}}</div>
  </div>
</template>

<script>
  export default {
    name: "Toast",
    props:{
      /*message:{
        type:String,
        default:""
      },
      show:{
        type:Boolean,
        default:false
      }*/
    },
    data(){
      return {
        message:'',
        isShow:false
      }
    },
    methods:{
      show(message,duration=2000){   // 给一个默认时间
        this.isShow=true;
        this.message=message;
        setTimeout(()=>{
          this.isShow=false;
          this.message=''
        },duration)
      }
    }
  }
</script>

